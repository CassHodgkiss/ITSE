);
CREATE TABLE IF NOT EXISTS "WithdrawDeposits" (
	"TransactionId"	INTEGER NOT NULL,
	"IsWithdrawn"	INTEGER NOT NULL,
	FOREIGN KEY("TransactionId") REFERENCES "Transactions"("Id")
);
CREATE TABLE IF NOT EXISTS "Transactions" (
	"Id"	INTEGER NOT NULL UNIQUE,
	"DateCreated"	TEXT NOT NULL,
	"Amount"	REAL NOT NULL,
	"AccountId"	INTEGER,
	PRIMARY KEY("Id" AUTOINCREMENT),
	FOREIGN KEY("AccountId") REFERENCES "Accounts"("Id")
);
CREATE TABLE IF NOT EXISTS "Transfers" (
	"TransactionId"	INTEGER NOT NULL,
	"IsRecieved"	INTEGER NOT NULL,
	"AccountOtherId"	INTEGER NOT NULL,
	FOREIGN KEY("AccountOtherId") REFERENCES "Accounts"("Id"),
	FOREIGN KEY("TransactionId") REFERENCES "Transactions"("Id")
);
CREATE TABLE IF NOT EXISTS "SpecialCustomers" (
	"CustomerId"	INTEGER NOT NULL,
	"OverdraftPercentage"	REAL NOT NULL,
	FOREIGN KEY("CustomerId") REFERENCES "Customers"("Id")
);
CREATE TABLE IF NOT EXISTS "Currents" (
	"AccountId"	INTEGER NOT NULL,
	FOREIGN KEY("AccountId") REFERENCES "Accounts"("Id")
);
CREATE TABLE IF NOT EXISTS "CreditCards" (
	"Id"	INTEGER NOT NULL UNIQUE,
	"CardNumberHash"	TEXT NOT NULL,
	"CreationDate"	TEXT NOT NULL,
	"ExpireDate"	TEXT NOT NULL,
	"CVV"	TEXT NOT NULL,
	"IsSwallowed"	INTEGER NOT NULL,
	PRIMARY KEY("Id" AUTOINCREMENT)
);

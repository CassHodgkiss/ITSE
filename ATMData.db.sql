CREATE TABLE IF NOT EXISTS "Customers" (
	"Id"	INTEGER NOT NULL UNIQUE,
	"FirstName"	TEXT NOT NULL,
	"LastName"	TEXT NOT NULL,
	"Address"	TEXT NOT NULL,
	"AnnualSalary"	NUMERIC NOT NULL,
